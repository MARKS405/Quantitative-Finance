    rolling_pto_returns.append(returns_pto_w)
    rolling_spo_returns.append(returns_spo_w)

    rolling_pto_weights.append(weights_pto_w)
    rolling_spo_weights.append(weights_spo_w)

    rolling_dates.append(X_test_w.index[0])

