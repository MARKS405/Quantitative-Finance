# 1. Función para calcular turnover

def calculate_turnover(weights):
    turnover = weights.diff().abs().sum(axis=1)
    turnover = turnover.fillna(0)
    return turnover

# 2. Función para calcular retornos netos de costos

def calculate_portfolio_returns_with_costs(weights, realized_returns, transaction_cost=0.005):

    gross_returns = (weights.shift(1) * realized_returns).sum(axis=1)
    gross_returns = gross_returns.dropna()

    turnover = calculate_turnover(weights)
    turnover = turnover.reindex(gross_returns.index).fillna(0)

    costs = transaction_cost * turnover

    net_returns = gross_returns - costs

    return net_returns, gross_returns, costs, turnover

# 3. Aplicar costos a PtO y SPO simplificado

