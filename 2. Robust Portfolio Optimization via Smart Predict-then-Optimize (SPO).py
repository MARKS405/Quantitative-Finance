# 2. Modelo simple:
# Recibe features y produce pesos long-only que suman 1

class SimpleSPO(nn.Module):
    def __init__(self, input_dim, n_assets):
        super(SimpleSPO, self).__init__()
        self.linear = nn.Linear(input_dim, n_assets)
        self.softmax = nn.Softmax(dim=1)

    def forward(self, x):
        scores = self.linear(x)
        weights = self.softmax(scores)
        return weights

# 3. Inicializar modelo

input_dim = X_train.shape[1]
n_assets = len(tickers)

spo_model = SimpleSPO(input_dim, n_assets)
optimizer = optim.Adam(spo_model.parameters(), lr=0.001)

# 4. Loss decision-focused
# En vez de minimizar error de predicción, maximizamos retorno del portafolio

def spo_loss(weights, realized_returns):
    portfolio_returns = torch.sum(weights * realized_returns, dim=1)
    loss = -torch.mean(portfolio_returns)
    return loss

# 5. Entrenamiento

epochs = 300

for epoch in range(epochs):
    spo_model.train()

    optimizer.zero_grad()

    weights_train = spo_model(X_train_tensor)
    loss = spo_loss(weights_train, y_train_tensor)

    loss.backward()
    optimizer.step()

    if epoch % 50 == 0:
        print(f"Epoch {epoch}, Loss: {loss.item():.6f}")

