# 1. Parámetros rolling

train_window = 252      # 1 año de entrenamiento
test_window = 21        # 1 mes de prueba
step_size = 21          # rebalanceo mensual

epochs = 150
learning_rate = 0.001

# 2. Función para entrenar SPO en cada ventana

def train_spo_model(X_train_w, y_train_w, input_dim, n_assets, epochs=150, lr=0.001):

    X_tensor = torch.tensor(X_train_w.values, dtype=torch.float32)
    y_tensor = torch.tensor(y_train_w.values, dtype=torch.float32)

    model = SimpleSPO(input_dim, n_assets)
    optimizer = optim.Adam(model.parameters(), lr=lr)

    for epoch in range(epochs):
        model.train()
        optimizer.zero_grad()

        weights = model(X_tensor)
        loss = spo_loss(weights, y_tensor)

        loss.backward()
        optimizer.step()

    return model

# 3. Función para entrenar PtO en cada ventana

def train_pto_model(X_train_w, y_train_w):
    model = LinearRegression()
    model.fit(X_train_w, y_train_w)
    return model

# 4. Ejecutar rolling window

rolling_pto_returns = []
rolling_spo_returns = []

rolling_pto_weights = []
rolling_spo_weights = []

rolling_dates = []

for start in range(0, len(X) - train_window - test_window, step_size):

    train_start = start
    train_end = start + train_window
    test_end = train_end + test_window

    X_train_w = X.iloc[train_start:train_end]
    y_train_w = y.iloc[train_start:train_end]

    X_test_w = X.iloc[train_end:test_end]
    y_test_w = y.iloc[train_end:test_end]

    # PtO
    pto_model_w = train_pto_model(X_train_w, y_train_w)
    y_pred_w = pd.DataFrame(
        pto_model_w.predict(X_test_w),
        index=X_test_w.index,
        columns=tickers
    )

    weights_pto_w = y_pred_w.apply(optimize_portfolio, axis=1)
    returns_pto_w = (weights_pto_w.shift(1) * y_test_w).sum(axis=1).dropna()

    # SPO
    spo_model_w = train_spo_model(
        X_train_w,
        y_train_w,
        input_dim=input_dim,
        n_assets=n_assets,
        epochs=epochs,
        lr=learning_rate
    )

    X_test_tensor_w = torch.tensor(X_test_w.values, dtype=torch.float32)

    with torch.no_grad():
        weights_spo_w_tensor = spo_model_w(X_test_tensor_w)

    weights_spo_w = pd.DataFrame(
        weights_spo_w_tensor.numpy(),
        index=X_test_w.index,
        columns=tickers
    )

