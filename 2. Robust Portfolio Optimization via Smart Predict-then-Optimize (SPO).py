def optimize_portfolio(returns_pred):

    # Long-only + suma 1
    weights = returns_pred.clip(lower=0)

    if weights.sum() == 0:
        weights = np.ones(len(weights)) / len(weights)
    else:
        weights = weights / weights.sum()

    return weights

