# Lagged returns
# The original code produced an error because returns.shift(i) returns a DataFrame with multiple columns (one for each ticker),
# but it was being assigned to a single new column 'lag_i'.
# To fix this, we will calculate the average daily return across all assets, and then apply the lag to this series.
daily_mean_returns = returns.mean(axis=1) # Calculate the mean return across all assets for each day
