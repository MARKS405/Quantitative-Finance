# 1. Función para contar trades

def count_trades_from_returns(strategy_returns):
    active = strategy_returns != 0
    trade_starts = active & (~active.shift(1).fillna(False))
    return trade_starts.sum()

# 2. Backtest con métricas adicionales

def backtest_basket_extended(weights_array, log_prices, entry_threshold=2.0, exit_threshold=0.0):

    weights_array = np.array(weights_array)
    weights_array = weights_array / np.sum(np.abs(weights_array))

    spread_temp = log_prices @ weights_array
    z_temp = (spread_temp - spread_temp.mean()) / spread_temp.std()

    signals_temp = pd.DataFrame(index=z_temp.index)
    signals_temp["zscore"] = z_temp
    signals_temp["position"] = 0

    current_position = 0

    for i in range(len(signals_temp)):
        z = signals_temp["zscore"].iloc[i]

        if current_position == 0:
            if z > entry_threshold:
                current_position = -1
            elif z < -entry_threshold:
                current_position = 1

        elif current_position == 1:
            if z >= exit_threshold:
                current_position = 0

        elif current_position == -1:
            if z <= exit_threshold:
                current_position = 0

        signals_temp.iloc[i, signals_temp.columns.get_loc("position")] = current_position

    asset_returns_temp = log_prices.diff().dropna()
    basket_returns_temp = asset_returns_temp @ weights_array

    strategy_returns_temp = signals_temp["position"].shift(1) * basket_returns_temp
    strategy_returns_temp = strategy_returns_temp.dropna()

    if strategy_returns_temp.std() == 0:
        return None

    cumulative_temp = (1 + strategy_returns_temp).cumprod()
    drawdown_temp = cumulative_temp / cumulative_temp.cummax() - 1

    annual_return_temp = strategy_returns_temp.mean() * 252
    annual_vol_temp = strategy_returns_temp.std() * np.sqrt(252)
    sharpe_temp = annual_return_temp / annual_vol_temp
    max_drawdown_temp = drawdown_temp.min()

    trades_temp = count_trades_from_returns(strategy_returns_temp)

    return {
        "returns": strategy_returns_temp,
        "sharpe": sharpe_temp,
        "annual_return": annual_return_temp,
        "annual_volatility": annual_vol_temp,
        "max_drawdown": max_drawdown_temp,
        "total_return": cumulative_temp.iloc[-1] - 1,
        "trades": trades_temp
    }

# 3. Nueva función objetivo robusta

def robust_objective(trial):

    weights_trial = [
        trial.suggest_float("w1", -1, 1),
        trial.suggest_float("w2", -1, 1),
        trial.suggest_float("w3", -1, 1)
    ]

    result = backtest_basket_extended(
        weights_trial,
        log_prices_train,
        entry_threshold=2.0,
        exit_threshold=0.0
    )

    if result is None:
        return -999

    sharpe = result["sharpe"]
    max_drawdown = abs(result["max_drawdown"])
    trades = result["trades"]

    trade_penalty = 0

    if trades < 5:
        trade_penalty = 1.0

    score = sharpe - 2.0 * max_drawdown - trade_penalty

    return score

