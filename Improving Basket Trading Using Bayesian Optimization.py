# 1. Backtest extendido con costos de transacción

def backtest_basket_with_costs(
    weights_array,
    log_prices,
    entry_threshold=2.0,
    exit_threshold=0.0,
    transaction_cost=0.001  # 10 bps por cambio de posición
):

    weights_array = np.array(weights_array)
    weights_array = weights_array / np.sum(np.abs(weights_array))

    spread_temp = log_prices @ weights_array
    z_temp = (spread_temp - spread_temp.mean()) / spread_temp.std()

    signals_temp = pd.DataFrame(index=z_temp.index)
    signals_temp["zscore"] = z_temp
    signals_temp["position"] = 0

    current_position = 0

    for i in range(len(signals_temp)):
        z = signals_temp["zscore"].iloc[i]

        if current_position == 0:
            if z > entry_threshold:
                current_position = -1
            elif z < -entry_threshold:
                current_position = 1

        elif current_position == 1:
            if z >= exit_threshold:
                current_position = 0

        elif current_position == -1:
            if z <= exit_threshold:
                current_position = 0

        signals_temp.iloc[i, signals_temp.columns.get_loc("position")] = current_position

    asset_returns_temp = log_prices.diff().dropna()
    basket_returns_temp = asset_returns_temp @ weights_array

    gross_returns = signals_temp["position"].shift(1) * basket_returns_temp
    gross_returns = gross_returns.dropna()

    position_changes = signals_temp["position"].diff().abs()
    position_changes = position_changes.reindex(gross_returns.index).fillna(0)

    costs = position_changes * transaction_cost

    net_returns = gross_returns - costs
    net_returns.name = "Net Strategy Return"

    return net_returns, gross_returns, costs, signals_temp

