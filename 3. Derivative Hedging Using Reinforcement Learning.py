def run_episode(env, policy):
    states = []
    actions = []
    rewards = []

    state = env.reset()
    done = False

    while not done:
        state_tensor = torch.tensor(state, dtype=torch.float32).unsqueeze(0)

        # Acción determinística base
        action_mean = policy(state_tensor)

        # Añadimos exploración con ruido
        noise = torch.normal(mean=0.0, std=0.10, size=action_mean.shape)
        action = torch.clamp(action_mean + noise, 0, 1)

        next_state, reward, done, info = env.step(action.item())

        states.append(state_tensor)
        actions.append(action)
        rewards.append(reward)

        state = next_state

    return states, actions, rewards

# 4. Discounted rewards

def compute_discounted_rewards(rewards, gamma=0.99):
    discounted = []
    cumulative = 0

    for rwd in reversed(rewards):
        cumulative = rwd + gamma * cumulative
        discounted.insert(0, cumulative)

    discounted = torch.tensor(discounted, dtype=torch.float32)

    if discounted.std() > 0:
        discounted = (discounted - discounted.mean()) / (discounted.std() + 1e-8)

    return discounted

