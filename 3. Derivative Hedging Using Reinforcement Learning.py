extended_episode_rewards = []

n_episodes = 1500
gamma = 0.99

for episode in range(n_episodes):
    states, actions, rewards = run_episode(extended_env, extended_policy)
    discounted_rewards = compute_discounted_rewards(rewards, gamma=gamma)

    loss = 0

    for state_tensor, action, Gt in zip(states, actions, discounted_rewards):
        action_pred = extended_policy(state_tensor)
        loss += ((action_pred - action.detach()) ** 2 * (-Gt)).mean()

    extended_optimizer.zero_grad()
    loss.backward()
    extended_optimizer.step()

    extended_episode_rewards.append(sum(rewards))

    if episode % 150 == 0:
        print(f"Episode {episode}, Extended Reward: {sum(rewards):.4f}")

