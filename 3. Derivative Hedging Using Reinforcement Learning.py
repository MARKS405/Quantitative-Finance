# 1. Función para simular una sola trayectoria

def simulate_single_gbm(S0, r, sigma, T, dt):
    N = int(T / dt)
    path = np.zeros(N)
    path[0] = S0

    for t in range(1, N):
        z = np.random.normal()
        path[t] = path[t-1] * np.exp((r - 0.5 * sigma**2) * dt + sigma * np.sqrt(dt) * z)

    return path

# 2. Clase del entorno de hedging

class HedgingEnv:
    def __init__(self, S0, K, T, r, sigma, dt, transaction_cost):
        self.S0 = S0
        self.K = K
        self.T = T
        self.r = r
        self.sigma = sigma
        self.dt = dt
        self.transaction_cost = transaction_cost
        self.N = int(T / dt)
        self.reset()

    def reset(self):
        self.path = simulate_single_gbm(
            self.S0, self.r, self.sigma, self.T, self.dt
        )
        self.t = 0
        self.position = 0.0

        return self._get_state()

    def _get_state(self):
        S = self.path[self.t]
        tau = max(self.T - self.t * self.dt, 0)

        moneyness = S / self.K

        state = np.array([
            moneyness,
            tau,
            self.position
        ], dtype=np.float32)

        return state

    def step(self, action):
        # Acción = hedge ratio entre 0 y 1
        action = float(np.clip(action, 0, 1))

        S_t = self.path[self.t]
        tau_t = max(self.T - self.t * self.dt, 0)
        V_t = bs_call_price(S_t, self.K, tau_t, self.r, self.sigma)

        previous_position = self.position
        new_position = action

        # Avanzar tiempo
        self.t += 1
        done = self.t >= self.N - 1

        S_next = self.path[self.t]
        tau_next = max(self.T - self.t * self.dt, 0)
        V_next = bs_call_price(S_next, self.K, tau_next, self.r, self.sigma)

        # P&L del hedge, inspirado en el PDF de MathWorks
        hedge_pnl = previous_position * (S_next - S_t)
        option_pnl = -(V_next - V_t)
        trading_cost = self.transaction_cost * abs(new_position - previous_position) * S_next

        reward = hedge_pnl + option_pnl - trading_cost

        # Penalización por swings grandes de P&L
        reward = reward - 1.5 * reward**2

        self.position = new_position

        next_state = self._get_state()

        return next_state, reward, done, {
            "S": S_next,
            "option_value": V_next,
            "position": self.position,
            "trading_cost": trading_cost
        }

