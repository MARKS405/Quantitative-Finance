# 1. Entorno extendido: penaliza pérdidas, costos, exceso de posición
# y desviación frente a Delta BSM

class ExtendedHedgingEnv(HedgingEnv):
    def __init__(
        self,
        S0, K, T, r, sigma, dt, transaction_cost,
        downside_penalty=5.0,
        position_penalty=0.01,
        delta_tracking_penalty=0.10
    ):
        super().__init__(S0, K, T, r, sigma, dt, transaction_cost)
        self.downside_penalty = downside_penalty
        self.position_penalty = position_penalty
        self.delta_tracking_penalty = delta_tracking_penalty

    def step(self, action):
        next_state, reward, done, info = super().step(action)

        S = info["S"]
        tau = max(self.T - self.t * self.dt, 0)
        position = info["position"]

        delta_bsm = bs_delta(S, self.K, tau, self.r, self.sigma)

        # Penalización asimétrica por pérdidas
        if reward < 0:
            reward = reward - self.downside_penalty * reward**2

        # Penalización por exceso de posición
        reward = reward - self.position_penalty * position**2

        # Penalización por alejarse demasiado de Delta BSM
        reward = reward - self.delta_tracking_penalty * (position - delta_bsm)**2

        return next_state, reward, done, info

