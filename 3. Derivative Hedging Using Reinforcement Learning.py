# 2. Función para evaluar delta hedging en una trayectoria

def delta_hedging_path(path, K, T, r, sigma, dt, transaction_cost):

    n_steps = len(path)

    option_values = []
    deltas = []

    cash = 0
    hedge_position = 0

    # Precio inicial de la opción
    option_price_0 = bs_call_price(path[0], K, T, r, sigma)

    # Vendemos la opción y recibimos prima
    cash += option_price_0

    for t in range(n_steps):
        tau = max(T - t * dt, 0)
        S = path[t]

        option_value = bs_call_price(S, K, tau, r, sigma)
        delta = bs_delta(S, K, tau, r, sigma)

        option_values.append(option_value)
        deltas.append(delta)

        # Rebalancear hedge hacia delta
        trade = delta - hedge_position
        cost = abs(trade) * S * transaction_cost

        cash -= trade * S
        cash -= cost

        hedge_position = delta

    # Liquidación final
    S_T = path[-1]
    payoff = max(S_T - K, 0)

    # Cerrar hedge
    cash += hedge_position * S_T
    cash -= abs(hedge_position) * S_T * transaction_cost

    # Pagamos payoff de la opción vendida
    cash -= payoff

    final_pnl = cash

    return final_pnl, np.array(option_values), np.array(deltas)

